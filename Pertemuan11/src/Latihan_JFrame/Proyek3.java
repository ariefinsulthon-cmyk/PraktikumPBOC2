    private void jSaveMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveMapActionPerformed
        // TODO add your handling code here:
        dataMap.clear();
        key = 1; // reset key
        for (int i = 0; i < dlm.getSize(); i++) {
            dataMap.put(key++, dlm.getElementAt(i));
        }
        jLabelMap.setText("Data Tersimpan : " + dataMap.size());
        
    }//GEN-LAST:event_jSaveMapActionPerformed

    private void jInsertDataMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsertDataMapActionPerformed
        // TODO add your handling code here:
        for(int key : dataMap.keySet()) {
            dlm.addElement(dataMap.get(key));
        }
        
    }//GEN-LAST:event_jInsertDataMapActionPerformed

