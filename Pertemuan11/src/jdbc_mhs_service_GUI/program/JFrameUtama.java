    private void btnAmbilSemuaDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAmbilSemuaDataActionPerformed
        // TODO add your handling code here:
        dlmId.clear();
        dlmNama.clear();
        
        MysqlMahasiswaService service = new MysqlMahasiswaService();
        List<Mahasiswa> list = service.getAll();
        
        for(Mahasiswa m : list){
            addItem(m.getId(), m.getNama());
        }
    }//GEN-LAST:event_btnAmbilSemuaDataActionPerformed

    private void btnTambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahkanActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText().trim();
        MysqlMahasiswaService service = new MysqlMahasiswaService();
        Mahasiswa mhs = service.makeMhsObject();
        mhs.setNama(nama);
        service.add(mhs);
        dlmId.clear();
        dlmNama.clear();
        List<Mahasiswa> list = service.getAll();
        for (Mahasiswa m : list) {
            addItem(m.getId(), m.getNama());
        }
        txtNama.setText("");
    }//GEN-LAST:event_btnTambahkanActionPerformed

