    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int index = listId.getSelectedIndex();
        String namaBaru = txtNamaEdit.getText().trim();
        int id = Integer.parseInt(dlmId.getElementAt(index));
        MysqlMahasiswaService service = new MysqlMahasiswaService();
        Mahasiswa mhs = service.makeMhsObject();
        mhs.setId(id);
        mhs.setNama(namaBaru);
        service.update(mhs);
        dlmNama.setElementAt(namaBaru, index);
        txtIdEdit.setText("");
        txtNamaEdit.setText("");
                     
        txtIdHapus.setText("");
        txtNamaHapus.setText("");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int index = listId.getSelectedIndex();
        int id = Integer.parseInt(dlmId.getElementAt(index));
        MysqlMahasiswaService service = new MysqlMahasiswaService();
        service.delete(id);
        dlmNama.removeElementAt(index);
        dlmId.removeElementAt(index);
        txtIdEdit.setText("");
        txtNamaEdit.setText("");
                
                
        txtIdHapus.setText("");
        txtNamaHapus.setText("");
                
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnResetIndeksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetIndeksActionPerformed
        // TODO add your handling code here:
        MysqlMahasiswaService service = new MysqlMahasiswaService();
       
        if(service.isEmpty()) {
            service.indexReset();
            labelIndexReset.setText("Index direset ke 1");
        }else {
            JOptionPane.showMessageDialog(this,
            "Tabel tidak kosong!\nHapus semua data dulu.",
            "Peringatan", JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_btnResetIndeksActionPerformed

